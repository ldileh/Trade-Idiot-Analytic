// Regenerate src/stocks.id.data.ts from the public IDX emiten directory.
// Source: wildangunawan/Dataset-Saham-IDX (maintained list of all IDX-listed
// companies). Yahoo Finance uses the ".JK" suffix for IDX symbols (e.g. BBCA.JK).
//
//   node scripts/gen-stocks-id.mjs
//
// Run again whenever you want to refresh the list (new listings/delistings).
import { writeFileSync } from "node:fs";

const URL_SRC =
  "https://raw.githubusercontent.com/wildangunawan/Dataset-Saham-IDX/master/List%20Emiten/all.csv";

function clean(name) {
  return name
    .replace(/\s+Tbk\.?$/i, "")
    .replace(/[,\s]+$/, "")
    .replace(/\s+/g, " ")
    .trim() || name.trim();
}

const res = await fetch(URL_SRC);
if (!res.ok) throw new Error(`${URL_SRC} -> ${res.status}`);
const rows = (await res.text()).split("\n").map((l) => l.trim()).filter(Boolean);

const list = [];
for (const row of rows) {
  if (row.startsWith("code,")) continue; // header
  const code = row.split(",")[0]?.trim();
  // name may contain commas -> take everything up to the listingDate field.
  const name = row.slice(code.length + 1).replace(/,\d{4}-\d{2}-\d{2}T.*$/, "");
  if (!code || !/^[A-Z]{3,5}$/.test(code)) continue;
  list.push({ sym: `${code}.JK`, name: clean(name) });
}
list.sort((a, b) => a.sym.localeCompare(b.sym));

const body = list.map((s) => `  { sym: ${JSON.stringify(s.sym)}, name: ${JSON.stringify(s.name)} },`).join("\n");
const out = `// AUTO-GENERATED by scripts/gen-stocks-id.mjs — do not edit by hand.
// All IDX-listed emiten (Yahoo ".JK" suffix). Source: Dataset-Saham-IDX.
// Regenerate: node scripts/gen-stocks-id.mjs
import type { Stock } from "./stocks";

export const ID_STOCKS: Stock[] = [
${body}
];
`;

writeFileSync(new URL("../src/stocks.id.data.ts", import.meta.url), out);
console.log(`Wrote src/stocks.id.data.ts with ${list.length} symbols.`);
